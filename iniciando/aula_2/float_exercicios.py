#Crie um programa que calcule a média de dois números flutuantes fornecidos pelo usuário.

valor_flutuante_1 = float(input("Digite o primeiro valor: "))
valor_flutuante_2 = float(input("Digite o segundo valor: "))

media = (valor_flutuante_1 + valor_flutuante_2) / 2

print(f"A media dos dois numeros é {media}")

#Desenvolva um programa que calcule a potência de um número (base e expoente fornecidos pelo usuário).

base = float(input("Digite a base que quer usar: "))
expoente = float(input("Digite o expoente que quer usar: "))

potencia = base ** expoente

print(f"Esse é o resultado da potencia {potencia}")

#Faça um programa que converta a temperatura de Celsius para Fahrenheit.

celsius = float(input("Digite os graus celsius: "))

fahrenheit = (celsius * 9/5) + 32

print(f"{celsius}°C é equivalente a {fahrenheit}°F")

#Escreva um programa que calcule a área de um círculo, recebendo o raio como entrada.

raio = float(input("Digite o raio: "))

area = 3.14 * raio ** 2

print(f"Area do circulo é: {area:.2f}")





